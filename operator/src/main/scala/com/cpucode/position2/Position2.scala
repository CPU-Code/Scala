object Position2 {
  def main(args: Array[String]): Unit = {
    // 运算符的本质
    val n1: Int = 10
    val n2: Int = 20

    println(n1.+(n2))
    println(n1 + n2 )

    println(1.34.*(25))
    println(1.34 * 25)

    println(7.5.toInt)
    println(7.5 toInt)
    println(7.5 toString)
  }

}
